  <section id="events">

    <h2>Dinner with Professors</h2>
      <p>
        Each semester SASE hosts a dinner with professors where students and
        professors meet in an informal environment to eat dinner and talk. SASE
        members are free to invite their own professors and in the past we have
        had faculty from the Chemistry, Computer Science, Biology, Mathematics,
        and Biological Engineering departments.
      </p>

    <h2>Games Night</h2>
      <p>
        Every semester SASE has a games night that is a fun way to unwind during
        the stressful semesters at Cornell. Favorite games include Bang, Risk,
        and Dominion. Anyone can bring their own game to play with members. It
        is a great way to meet and connect with executive board members.
      </p>

    <h2>Asia Night</h2>
      <p>
        Cornell Asian/Pacific Islander Student Union hosts Asia Night annually
        where over 50 student organizations hold booths and perform cultural
        displays. SASE holds a booth where anyone can make and decorate their own
        rattle drum!
      </p>

    <h2>Engineering Day at the Mall</h2>
      <p>
        SASE coordinates with the Society of Women in Engineering and other
        engineering organizations to participate in engineering day at the mall.
        Hands on activities and experiments show kids (and adults) at the mall just
        how fun science and engineering can be.
      </p>

    <h2>Resume Workshops</h2>
      <p>
        An online resume critique with P&G helps members bolster their resume to
        prepare themselves for the career fair in the fall in order to get jobs and
        internships. Though all people can submit their resume, SASE members get
        priority and a faster response time.
      </p>

    <h2>Graduate School Panel</h2>
      <p>
        For members who wish to attend graduate school (not medical school) this
        helpful panel of graduate students shared their experiences of applying to
        graduate school, the visiting and acceptance process, and how to survive
        once in graduate school. The panel is fairly informal and gives plenty of
        time for people to ask questions.
      </p>

    <h2>Information Sessions with Companies</h2>
      <p>
        SASE hosted information sessions with P&G, Pearson, and Epic last year. These give members opportunities to connect with recruiters in order to get interviews for internships as well as jobs after graduation.
      </p>

    <h2>Regional Conference</h2>
      <p>
        The 3rd annual regional conference was this spring in Binghamton. SASE 
        members from MIT, Boston University, Clarkson, Cornell, Binghamton, and 
        many other universities in the area attended, including some people who 
        want to start chapters at their university. Every year the conference gets 
        bigger with more chapters coming. There are workshops, panels, speakers, 
        and great networking opportunities to help build members professionally.

    <h2>National Conference</h2>
      <p>
        Next semester in the fall, the national conference will be in Philadelphia.
        The national conference will provide exciting speakers, engaging workshops 
        and panels, and a career fair with over 40 companies looking for strong 
        engineers and scientists for full time and internship positions. SASE 
        members from universities all over America, SASE national coordinators and
        company recruiters attend.
      </p>

